'use strict'

//Importaciones 
import mongoose from "mongoose";

const adminSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    },
    status: {
        type: String,
        enum: ['active', 'inactive'],
        default: 'active'
    },
    creationDate: {
        type: Date,
        default: Date.now
    }
})

export default mongoose.model('Admin', adminSchema)